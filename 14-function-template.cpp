#include <bits/stdc++.h>
using namespace std;

template <class myTemplate>
myTemplate add(myTemplate a, myTemplate b)
{
    return a + b;
}

int main()
{
    cout << "Addition of int number is : " << add(5, 26) << endl;
    cout << "Addition of Float number is : " << add(5.5559, 26.555) << endl;

    return 0;
}